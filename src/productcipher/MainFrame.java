    private void encryptbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_encryptbtnActionPerformed
        if(efilepathlbl.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Choose a file");
        }else{
             //get the text need to encrypt
        fileText=encryptText.toString();
        Encrypt encrypt1=new Encrypt();
        //get a random key
        key=encrypt1.getKey(fileText.length()-1);
        //encrypt the text
        encrypted=encrypt1.encrypt(fileText, key);
        
        //show in UI
        keylbl.setText(key);
        encrypttxt.setText(encrypted);
        
        //store in a file
        try {
           FileOutputStream output = new FileOutputStream("encrypt.txt");
           output.write(encrypted.getBytes());
           output.close();
        } catch (IOException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        }
       
        
    }//GEN-LAST:event_encryptbtnActionPerformed

