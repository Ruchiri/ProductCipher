    private void echoosebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_echoosebtnActionPerformed
          //add file chooser
        encryptFilePath="";
        JFileChooser fileChooser=new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("TEXT FILES", "txt", "text");
        fileChooser.setFileFilter(filter);
        
        int status= fileChooser.showOpenDialog(null);
        //select file
        if(status==JFileChooser.APPROVE_OPTION){
            File selectedFile= fileChooser.getSelectedFile();
            encryptFilePath=selectedFile.getAbsolutePath();
            efilepathlbl.setText(encryptFilePath);
            
            try {
                //store the text need to encrypt
                InputStream inputStream=new FileInputStream(selectedFile);
                BufferedReader bufferedReader=new BufferedReader(new InputStreamReader(inputStream));
                
                String line= bufferedReader.readLine();
                encryptText= new StringBuilder();
                
                while(line!= null){
                    encryptText.append(line).append("\n");
                    line=bufferedReader.readLine();
                }
                bufferedReader.close();
                
            } catch (FileNotFoundException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_echoosebtnActionPerformed

